-- =============================================================================
-- Ставки акциза на легковые автомобили (в т.ч. электромобили)
-- Источник: НК РФ ст. 193, пп. 27–39 п. 1
-- Актуальные ставки 2026 года: Федеральный закон № 425-ФЗ от 28.11.2025
-- Ставка указана в руб. за 1 л.с.
-- Применимо и к электромобилям: акциз исчисляется от мощности электродвигателя
-- =============================================================================
CREATE TABLE excise_rates (
    id                      BIGSERIAL       PRIMARY KEY,
    engine_power_from_hp    INT             NOT NULL,
    engine_power_to_hp      INT,
    rate_rub_per_hp         DECIMAL(10,2)   NOT NULL,
    valid_from              DATE            NOT NULL,
    valid_to                DATE,

    CHECK (engine_power_to_hp IS NULL OR engine_power_to_hp >= engine_power_from_hp),
    CHECK (valid_to IS NULL OR valid_to >= valid_from)
);

CREATE INDEX idx_excise_valid ON excise_rates(valid_from, valid_to);

COMMENT ON TABLE excise_rates IS 'Ставки акциза на легковые автомобили (НК РФ ст. 193, ФЗ № 425-ФЗ от 28.11.2025)';
COMMENT ON COLUMN excise_rates.rate_rub_per_hp IS 'Ставка акциза в рублях за 1 л.с. (0,75 кВт)';

